# find the maximum and minimum value in the list
numbers = []
while True:
    x = int(input("Input numbers (-ve number ends the input loop"))
    if x < 0:
        break
    numbers.append(x)

print(numbers)
print("The minimum number in the list is : ", min(numbers))
print("The maximum number in the list is : ", max(numbers))
print("The maximum number in the list is : ", sum(numbers))
