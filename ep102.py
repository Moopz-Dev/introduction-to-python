# mathematical functions

import math

x = min(3, 4, 5, 6, -15)
print(x)

y = max(123, 43, -12, 111)
print(y)

z = abs(-100.2)
print(z)

p = pow(5, 2)
print(p)

s = math.sqrt(625)
print(s)

f = math.floor(143.56564)
print(f)

c = math.ceil(123.15431)
print(c)

print(math.pi)

# trigonometry
rad = math.radians(45)
x = math.sin(rad)
print(x)

x = math.cos(rad)
print(x)

x = math.tan(rad)
print(x)

# linear distance of two points
point1 = [0, 0]
point2 = [5, 12]
d = math.dist(point1, point2)
print(d)

# logarithm

x = math.log10(10000)
print(x)
