# comparing values

x, y, z = 100, 15, 30

print("x > y ? : ", x > y)
print("x < y ? : ", x < y)
print("x equals y ? : ", x == y)
print("x does not equal y ? : ", x != y)
print("x is greater than or equals y ? : ", x >= y)
print("x is less than or equals y ? : ", x <= y)
print("x is less than y which is less than z ? :", x < y < z)
